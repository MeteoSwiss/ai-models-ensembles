"""Generate the headline 8-way CRPSS table (9 rows: 4 trained-prob + 4
post-hoc + 1 classical) and write it as a standalone .tex file ready to
\\input into main.tex.

Adds ESFM and aifs_perturbed to the 7-way pattern. ESFM and aifs_perturbed
are read from per-baseline `crps_line_*_by_lead_ensprob.csv` files instead
of from the SwissClim intercomparison temporal_metrics_combined.csv,
because the combined CSV is regenerated by the intercomp pipeline which is
slow to re-run; the per-baseline path is up-to-date as soon as each
baseline's `evaluate_baselines.sh` finishes.

Output: /users/sadamov/pyprojects/ai-models-ensembles/figures/headline_8way_table.tex
"""

